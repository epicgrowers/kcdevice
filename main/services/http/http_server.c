static cJSON *create_sensors_object_from_cache(const sensor_cache_t *cache)
{
    cJSON *sensors = cJSON_CreateObject();
    if (sensors == NULL || cache == NULL) {
        return sensors;
    }

    for (uint8_t i = 0; i < cache->sensor_count && i < 8; i++) {
        const cached_sensor_t *sensor = &cache->sensors[i];
        if (!sensor->valid) {
            continue;
        }

        if (sensor->value_count == 1) {
            cJSON_AddNumberToObject(sensors, sensor->sensor_type, sensor->values[0]);
        } else if (sensor->value_count > 1) {
            cJSON *sensor_obj = cJSON_CreateObject();
            if (sensor_obj == NULL) {
                continue;
            }

            if (strcmp(sensor->sensor_type, "HUM") == 0) {
                if (sensor->value_count >= 1) cJSON_AddNumberToObject(sensor_obj, "humidity", sensor->values[0]);
                if (sensor->value_count >= 2) cJSON_AddNumberToObject(sensor_obj, "air_temp", sensor->values[1]);
                if (sensor->value_count >= 3) cJSON_AddNumberToObject(sensor_obj, "dew_point", sensor->values[2]);
            } else if (strcmp(sensor->sensor_type, "EC") == 0) {
                if (sensor->value_count >= 1) cJSON_AddNumberToObject(sensor_obj, "conductivity", sensor->values[0]);
                if (sensor->value_count >= 2) cJSON_AddNumberToObject(sensor_obj, "tds", sensor->values[1]);
                if (sensor->value_count >= 3) cJSON_AddNumberToObject(sensor_obj, "salinity", sensor->values[2]);
                if (sensor->value_count >= 4) cJSON_AddNumberToObject(sensor_obj, "specific_gravity", sensor->values[3]);
            } else if (strcmp(sensor->sensor_type, "DO") == 0) {
                if (sensor->value_count >= 1) cJSON_AddNumberToObject(sensor_obj, "dissolved_oxygen", sensor->values[0]);
                if (sensor->value_count >= 2) cJSON_AddNumberToObject(sensor_obj, "saturation", sensor->values[1]);
            } else {
                for (uint8_t j = 0; j < sensor->value_count; j++) {
                    char field_name[16];
                    snprintf(field_name, sizeof(field_name), "value_%d", j);
                    cJSON_AddNumberToObject(sensor_obj, field_name, sensor->values[j]);
                }
            }

            cJSON_AddItemToObject(sensors, sensor->sensor_type, sensor_obj);
        }
    }

    return sensors;
}

static const char *mqtt_state_to_string(mqtt_state_t state)
{
    switch (state) {
        case MQTT_STATE_DISCONNECTED:
            return "disconnected";
        case MQTT_STATE_CONNECTING:
            return "connecting";
        case MQTT_STATE_CONNECTED:
            return "connected";
        case MQTT_STATE_ERROR:
            return "error";
        default:
            return "unknown";
    }
}

static bool http_sample_cpu_usage(float *out_percent)
{
    if (out_percent == NULL) {
        return false;
    }

#if CONFIG_FREERTOS_USE_TRACE_FACILITY && CONFIG_FREERTOS_GENERATE_RUN_TIME_STATS
    const UBaseType_t task_count = uxTaskGetNumberOfTasks();
    if (task_count == 0) {
        return false;
    }

    TaskStatus_t *status_array = calloc(task_count, sizeof(TaskStatus_t));
    if (status_array == NULL) {
        return false;
    }

    uint32_t total_run_time = 0;
    const UBaseType_t captured = uxTaskGetSystemState(status_array, task_count, &total_run_time);
    if (captured == 0 || total_run_time == 0) {
        free(status_array);
        return false;
    }

#if CONFIG_FREERTOS_UNICORE
    TaskHandle_t idle_handles[1] = {xTaskGetIdleTaskHandle()};
    const int idle_count = 1;
#else
    TaskHandle_t idle_handles[portNUM_PROCESSORS] = {0};
    for (int core = 0; core < portNUM_PROCESSORS; ++core) {
        idle_handles[core] = xTaskGetIdleTaskHandleForCore(core);
    }
    const int idle_count = portNUM_PROCESSORS;
#endif

    uint32_t idle_run_time = 0;
    for (UBaseType_t i = 0; i < captured; ++i) {
        for (int cpu = 0; cpu < idle_count; ++cpu) {
            if (status_array[i].xHandle == idle_handles[cpu]) {
                idle_run_time += status_array[i].ulRunTimeCounter;
            }
        }
    }

    free(status_array);

    static uint32_t s_prev_total_runtime = 0;
    static uint32_t s_prev_idle_runtime = 0;
    if (s_prev_total_runtime == 0) {
        s_prev_total_runtime = total_run_time;
        s_prev_idle_runtime = idle_run_time;
        return false;
    }

    const uint32_t delta_total = total_run_time - s_prev_total_runtime;
    const uint32_t delta_idle = idle_run_time - s_prev_idle_runtime;

    s_prev_total_runtime = total_run_time;
    s_prev_idle_runtime = idle_run_time;

    if (delta_total == 0 || delta_idle > delta_total) {
        return false;
    }

    float usage = 100.0f * (1.0f - ((float)delta_idle / (float)delta_total));
    if (usage < 0.0f) {
        usage = 0.0f;
    } else if (usage > 100.0f) {
        usage = 100.0f;
    }

    *out_percent = usage;
    return true;
#else
    return false;
#endif
}

