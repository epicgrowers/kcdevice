/**
 * @brief Handler for CA certificate download
 */
static esp_err_t ca_cert_handler(httpd_req_t *req)
{
    nvs_handle_t nvs_handle;
    esp_err_t err = nvs_open_from_partition("https", "https", NVS_READONLY, &nvs_handle);
    if (err != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_404_NOT_FOUND, "CA certificate not found");
        return ESP_FAIL;
    }
    
    size_t ca_cert_len = 0;
    err = nvs_get_str(nvs_handle, "ca_cert", NULL, &ca_cert_len);
    if (err != ESP_OK || ca_cert_len == 0) {
        nvs_close(nvs_handle);
        httpd_resp_send_err(req, HTTPD_404_NOT_FOUND, "CA certificate not available");
        return ESP_FAIL;
    }
    
    char *ca_cert = malloc(ca_cert_len);
    if (ca_cert == NULL) {
        nvs_close(nvs_handle);
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "Out of memory");
        return ESP_FAIL;
    }
    
    err = nvs_get_str(nvs_handle, "ca_cert", ca_cert, &ca_cert_len);
    nvs_close(nvs_handle);
    
    if (err != ESP_OK) {
        free(ca_cert);
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "Failed to read CA certificate");
        return ESP_FAIL;
    }
    
    // Set content type and disposition for download
    httpd_resp_set_type(req, "application/x-pem-file");
    httpd_resp_set_hdr(req, "Content-Disposition", "attachment; filename=\"kannacloud-ca.crt\"");
    
    httpd_resp_send(req, ca_cert, ca_cert_len - 1);
    free(ca_cert);
    
    return ESP_OK;
}

static const httpd_uri_t ca_cert_uri = {
    .uri = "/ca.crt",
    .method = HTTP_GET,
    .handler = ca_cert_handler,
    .user_ctx = NULL
};

esp_err_t http_server_start(const http_server_config_t *config)
{
    if (config == NULL) {
        ESP_LOGE(TAG, "HTTPS server configuration missing parameters");
        return ESP_ERR_INVALID_ARG;
    }

    if (s_server != NULL) {
        ESP_LOGW(TAG, "HTTPS server already running");
        return ESP_OK;
    }

    s_http_config = *config;
    
    // Initialize FATFS-backed dashboard storage
    web_editor_init_fs();
    
    ESP_LOGI(TAG, "Starting HTTPS server...");
    
    // Get certificates from cloud provisioning
    char *certificate = malloc(SERVICES_TLS_CERT_MAX_BYTES);
    char *private_key = malloc(SERVICES_TLS_KEY_MAX_BYTES);
    
    if (certificate == NULL || private_key == NULL) {
        ESP_LOGE(TAG, "Failed to allocate memory for certificates");
        free(certificate);
        free(private_key);
        return ESP_ERR_NO_MEM;
    }
    
    size_t cert_len = 0;
    size_t key_len = 0;
    esp_err_t err = services_provisioning_load_https_certificate(certificate, &cert_len);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to get certificate: %s", esp_err_to_name(err));
        free(certificate);
        free(private_key);
        return err;
    }
    
    err = services_provisioning_load_https_private_key(private_key, &key_len);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to get private key: %s", esp_err_to_name(err));
        free(certificate);
        free(private_key);
        return err;
    }
    
    ESP_LOGI(TAG, "Certificate length: %zu bytes", cert_len);
    ESP_LOGI(TAG, "Private key length: %zu bytes", key_len);
    
    // Debug certificate format
    ESP_LOGI(TAG, "Cert first 100 chars: %.100s", certificate);
    if (cert_len > 100) {
        ESP_LOGI(TAG, "Cert last 100 chars: %s", certificate + cert_len - 100);
    }
    
    // Configure HTTPS server
    httpd_ssl_config_t ssl_cfg = HTTPD_SSL_CONFIG_DEFAULT();
    ssl_cfg.httpd.max_uri_handlers = 50;  // Increased for web file editor + sensor action endpoints + new diagnostic endpoints
    ssl_cfg.httpd.stack_size = 8192;  // Reduced stack to save memory
    ssl_cfg.httpd.max_open_sockets = 3;  // Allow multiple connections now that PSRAM is enabled
    ssl_cfg.httpd.lru_purge_enable = true;  // Enable automatic cleanup of old connections
    ssl_cfg.httpd.close_fn = NULL;  // Use default close function
    ssl_cfg.httpd.recv_wait_timeout = 30;  // Increased timeout for SSL handshake (was 10)
    ssl_cfg.httpd.send_wait_timeout = 30;  // Increased timeout for SSL handshake (was 10)
    ssl_cfg.port_insecure = 0;  // Disable insecure port (HTTPS only)
    ssl_cfg.httpd.uri_match_fn = httpd_uri_match_wildcard;  // Enable wildcard URI matching
    ssl_cfg.httpd.server_port = s_http_config.https_port;
    
    // Set certificates (PEM format from NVS is already null-terminated)
    // mbedTLS needs length + 1 to include the null terminator
    ssl_cfg.servercert = (const uint8_t *)certificate;
    ssl_cfg.servercert_len = cert_len + 1;
    ssl_cfg.prvtkey_pem = (const uint8_t *)private_key;
    ssl_cfg.prvtkey_len = key_len + 1;
    
    // Skip client certificate verification (allows browsers to connect without trusting cert)
    ssl_cfg.session_tickets = false;  // Disable session resumption for testing
    ssl_cfg.use_secure_element = false;  // Not using hardware secure element
    
    // Start server
    err = httpd_ssl_start(&s_server, &ssl_cfg);
    
    // Clean up certificate buffers
    free(certificate);
    free(private_key);
    
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to start HTTPS server: %s", esp_err_to_name(err));
        return err;
    }
    
    // Register URI handlers
    httpd_register_uri_handler(s_server, &favicon_uri);
    httpd_register_uri_handler(s_server, &root_uri);
    httpd_register_uri_handler(s_server, &ca_cert_uri);  // CA certificate download
    httpd_register_uri_handler(s_server, &api_status_uri);
    httpd_register_uri_handler(s_server, &api_mqtt_status_uri);
    httpd_register_uri_handler(s_server, &api_clear_wifi_uri);
    httpd_register_uri_handler(s_server, &api_reboot_uri);
    httpd_register_uri_handler(s_server, &api_test_mqtt_uri);
    httpd_register_uri_handler(s_server, &api_settings_get_uri);
    httpd_register_uri_handler(s_server, &api_settings_post_uri);
    httpd_register_uri_handler(s_server, &api_settings_reset_uri);
    httpd_register_uri_handler(s_server, &api_webfiles_list_uri);
    httpd_register_uri_handler(s_server, &api_webfiles_reset_uri);
    httpd_register_uri_handler(s_server, &api_webfiles_get_uri);
    httpd_register_uri_handler(s_server, &api_webfiles_put_uri);
    httpd_register_uri_handler(s_server, &api_firmware_upload_uri);

    if (http_handlers_sensors_init(s_server) != ESP_OK) {
        ESP_LOGE(TAG, "Failed to register sensor HTTP endpoints");
    }

    esp_err_t ws_err = http_websocket_init(s_server);
    if (ws_err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to initialize WebSocket endpoint: %s", esp_err_to_name(ws_err));
    } else {
        esp_err_t listener_err = sensor_pipeline_register_snapshot_listener(
            NULL,
            http_websocket_snapshot_handler,
            NULL);
        if (listener_err != ESP_OK) {
            ESP_LOGE(TAG, "Failed to register sensor snapshot listener: %s", esp_err_to_name(listener_err));
            http_websocket_deinit();
        }
    }

    ESP_LOGI(TAG, "Registered sensor + dashboard API endpoints");
    
    return ESP_OK;
}

esp_err_t http_server_stop(void)
{
    if (s_server == NULL) {
        return ESP_OK;
    }
    
    http_websocket_deinit();
    sensor_pipeline_register_snapshot_listener(NULL, NULL, NULL);

    ESP_LOGI(TAG, "Stopping HTTPS server");
    httpd_ssl_stop(s_server);
    s_server = NULL;
    
    return ESP_OK;
}

bool http_server_is_running(void)
{
    return (s_server != NULL);
}

