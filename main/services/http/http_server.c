/**
 * @brief Root handler - serve dashboard from on-device filesystem or embedded fallback
 */
static esp_err_t root_handler(httpd_req_t *req)
{
    httpd_resp_set_type(req, "text/html");
    httpd_resp_set_hdr(req, "Connection", "keep-alive");
    httpd_resp_set_hdr(req, "Cache-Control", "no-cache, no-store, must-revalidate");
    httpd_resp_set_hdr(req, "Pragma", "no-cache");
    httpd_resp_set_hdr(req, "Expires", "0");
    
    // Try to load from the writable filesystem first
    char *content = NULL;
    size_t size = 0;
    if (web_editor_load_file("index.html", &content, &size) == ESP_OK) {
        ESP_LOGI(TAG, "Serving index.html from FATFS (%d bytes)", size);
        httpd_resp_send(req, content, size);
        free(content);
        return ESP_OK;
    }
    
    // Fallback to embedded HTML
    ESP_LOGI(TAG, "Serving index.html from embedded image (filesystem read failed)");
    const size_t html_size = index_html_end - index_html_start;
    httpd_resp_send(req, (const char *)index_html_start, html_size);
    return ESP_OK;
}

/**
 * @brief API status endpoint - return device status as JSON
 */
static esp_err_t api_status_handler(httpd_req_t *req)
{
    cJSON *root = cJSON_CreateObject();
    
    // Device ID
    char device_id[SERVICES_DEVICE_ID_MAX_LEN] = {0};
    if (services_provisioning_load_device_id(device_id, sizeof(device_id)) == ESP_OK &&
        device_id[0] != '\0') {
        cJSON_AddStringToObject(root, "device_id", device_id);
    } else {
        cJSON_AddStringToObject(root, "device_id", "unknown");
    }
    
    // WiFi SSID
    provisioning_saved_network_info_t saved_network = {0};
    if (provisioning_get_saved_network(&saved_network) == ESP_OK && saved_network.has_credentials) {
        cJSON_AddStringToObject(root, "wifi_ssid", saved_network.ssid);
    } else {
        cJSON_AddStringToObject(root, "wifi_ssid", "Not configured");
    }
    
    // IP Address
    if (provisioning_wifi_is_connected()) {
        char ip_str[16] = "Connected";
        esp_netif_t *sta_netif = esp_netif_get_handle_from_ifkey("WIFI_STA_DEF");
        if (sta_netif != NULL) {
            esp_netif_ip_info_t ip_info = {0};
            if (esp_netif_get_ip_info(sta_netif, &ip_info) == ESP_OK) {
                snprintf(ip_str, sizeof(ip_str), IPSTR, IP2STR(&ip_info.ip));
            }
        }
        cJSON_AddStringToObject(root, "ip_address", ip_str);
    } else {
        cJSON_AddStringToObject(root, "ip_address", "Disconnected");
    }
    
    // WiFi RSSI
    if (provisioning_wifi_is_connected()) {
        wifi_ap_record_t ap_info;
        if (esp_wifi_sta_get_ap_info(&ap_info) == ESP_OK) {
            cJSON_AddNumberToObject(root, "rssi", ap_info.rssi);
        }
    }
    
    // Uptime
    cJSON_AddNumberToObject(root, "uptime", esp_timer_get_time() / 1000000);
    
    // Current time
    char time_str[64];
    if (time_sync_get_time_string(time_str, sizeof(time_str), NULL) == ESP_OK) {
        cJSON_AddStringToObject(root, "current_time", time_str);
    } else {
        cJSON_AddStringToObject(root, "current_time", "Not synced");
    }
    
    // Free heap
    cJSON_AddNumberToObject(root, "free_heap", esp_get_free_heap_size());
    
    // CPU usage (derived from FreeRTOS run-time stats)
    float cpu_usage = 0.0f;
    if (http_sample_cpu_usage(&cpu_usage)) {
        cJSON_AddNumberToObject(root, "cpu_usage", cpu_usage);
    } else {
        cJSON_AddNumberToObject(root, "cpu_usage", -1);
    }
    
    sensor_pipeline_snapshot_t snapshot = {0};
    if (sensor_pipeline_snapshot(NULL, &snapshot) == ESP_OK) {
        cJSON_AddBoolToObject(root, "sensors_ready", snapshot.readiness.sensors_ready);
        cJSON_AddBoolToObject(root, "sensor_task_launched", snapshot.readiness.sensor_task_launched);
        cJSON_AddNumberToObject(root, "sensor_interval_sec", snapshot.reading_interval_sec);

        if (snapshot.cache_valid) {
            const sensor_cache_t *cache = &snapshot.cache;
            if (cache->battery_valid) {
                cJSON_AddNumberToObject(root, "battery", cache->battery_percentage);
            }

            cJSON *sensors = create_sensors_object_from_cache(cache);
            if (sensors != NULL) {
                cJSON_AddItemToObject(root, "sensors", sensors);
            }
        }
    }
    
    // Send JSON response
    const char *json_str = cJSON_PrintUnformatted(root);
    httpd_resp_set_type(req, "application/json");
    httpd_resp_send(req, json_str, HTTPD_RESP_USE_STRLEN);
    
    free((void *)json_str);
    cJSON_Delete(root);
    return ESP_OK;
}

static esp_err_t api_mqtt_status_handler(httpd_req_t *req)
{
    mqtt_connection_metrics_t metrics = {0};
    mqtt_connection_controller_get_metrics(&metrics);

    cJSON *root = cJSON_CreateObject();
    if (root == NULL) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "OOM");
        return ESP_ERR_NO_MEM;
    }

    const char *state_str = mqtt_state_to_string(metrics.state);
    cJSON_AddStringToObject(root, "state", state_str);
    cJSON_AddNumberToObject(root, "state_code", metrics.state);
    cJSON_AddBoolToObject(root, "should_run", metrics.should_run);
    cJSON_AddBoolToObject(root, "client_running", metrics.client_running);
    cJSON_AddBoolToObject(root, "supervisor_running", metrics.supervisor_running);
    cJSON_AddBoolToObject(root, "connected", metrics.state == MQTT_STATE_CONNECTED);
    cJSON_AddNumberToObject(root, "reconnect_count", metrics.reconnect_count);
    cJSON_AddNumberToObject(root, "consecutive_failures", metrics.consecutive_failures);
    cJSON_AddNumberToObject(root, "last_transition_us", (double)metrics.last_transition_us);

    if (metrics.last_transition_us > 0) {
        int64_t delta_us = esp_timer_get_time() - metrics.last_transition_us;
        if (delta_us < 0) {
            delta_us = 0;
        }
        double delta_sec = (double)delta_us / 1000000.0;
        cJSON_AddNumberToObject(root, "seconds_since_transition", delta_sec);
    } else {
        cJSON_AddNumberToObject(root, "seconds_since_transition", -1);
    }

    char *json_str = cJSON_PrintUnformatted(root);
    httpd_resp_set_type(req, "application/json");
    httpd_resp_send(req, json_str, HTTPD_RESP_USE_STRLEN);

    free(json_str);
    cJSON_Delete(root);
    return ESP_OK;
}

